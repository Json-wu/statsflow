/**
 * Generates _locales/{ja,ru,id,...}/messages.json from English template + inline translations.
 * Run: node scripts/build-extra-locales.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const enPath = path.join(root, '_locales/en/messages.json');
const en = JSON.parse(fs.readFileSync(enPath, 'utf8'));

function applyMessages(localeId, strings) {
  const out = {};
  for (const key of Object.keys(en)) {
    const tpl = en[key];
    const msg = strings[key];
    if (!msg) {
      throw new Error(`${localeId}: missing key ${key}`);
    }
    out[key] = { ...tpl, message: msg };
  }
  const dir = path.join(root, '_locales', localeId);
  fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(path.join(dir, 'messages.json'), JSON.stringify(out, null, 2) + '\n', 'utf8');
  console.log('wrote', localeId, Object.keys(out).length);
}

